// 测试decodeMap是否设置正确
static bool checkDecodeMap(const char* encodeMap, const Byte* decodeMap) {
    Byte goodDecodeMap[256];
    for (auto& b : goodDecodeMap) {
        b = 0xff;
    }
    for (int i = 0; i < 16; ++i) {
        goodDecodeMap[encodeMap[i]] = i;
    }
    encodeMap = "0123456789ABCDEF";
    for (int i = 0; i < 16; ++i) {
        goodDecodeMap[encodeMap[i]] = i;
    }
    for (int i = 0; i < 256; ++i) {
        if (goodDecodeMap[i] != decodeMap[i]) {
            return false;
        }
    }
    return true;
}

/**
 * 将字节数组转换为16进制字符串（小写，不带前缀0x）
 * @param bs 字节数组
 * @return 对应的16进制字符串
 */
std::string toHex(BytesConstRef bs) {
    // 提前分配好内存
    std::string ret(bs.size() * 2, '\0');

    // 每一个字节转换为两个字符
    size_t off = 0;
    for (auto b : bs) {
        ret[off++] = encodeMap[(b >> 4) & 0x0f];
        ret[off++] = encodeMap[b & 0x0f];
    }

    return ret;
}

/**
 * 将字节数组转换为16进制字符串（小写，带前缀0x）
 * @param bs 字节数组
 * @return 对应的16进制字符串
 */
std::string toHex0x(BytesConstRef bs) {
    // 提前分配好内存
    std::string ret = "0x";
    ret.resize(bs.size() * 2 + 2);

    // 每一个字节转换为两个字符
    size_t off = 2;
    for (auto b : bs) {
        ret[off++] = encodeMap[(b >> 4) & 0x0f];
        ret[off++] = encodeMap[b & 0x0f];
    }

    return ret;
}

/**
 * 将16进制字符串转换为字节数组
 * @param hex 16进制字符串（允许前缀0x或0X，不允许空白符）
 * @return 对应的字节数组
 * @throw 遇到非法16进制字符抛出BadHexCh异常
 */
Bytes fromHex(const std::string& hex) {
    assert(checkDecodeMap(s_encodeMap, s_decodeMap));

    // 跳过0x开头
    size_t endCh = hex.size();
    size_t curCh = endCh >= 2 && hex[0] == '0' && tolower(hex[1]) == 'x' ? 2 : 0;

    // 提前分配好内存
    Bytes ret((endCh - curCh + 1) / 2);
    size_t curByte = 0;

    // 奇数个字符，先处理一个
    if (endCh % 2) {
        Byte l = s_decodeMap[hex[curCh++]];
        if (l == 0xff) {
            throw BadHexCh();
        }
        ret[curByte++] = l;
    }

    // 每两个字符转换为一个字节
    while (curCh != endCh) {
        int h = s_decodeMap[hex[curCh++]];
        int l = s_decodeMap[hex[curCh++]];
        if (h == 0xff || l == 0xff) {
            throw BadHexCh();
        }
        ret[curByte++] = h << 4 | l;
    }

    return ret;
}

